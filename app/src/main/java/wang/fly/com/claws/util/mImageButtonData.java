public class mImageButtonData {
    private String mBtText;
    private int mBtImage;

    public mImageButtonData(String mBtText, int mBtImage) {
        this.mBtText = mBtText;
        this.mBtImage = mBtImage;
    }

    public String getmBtText() {
        return mBtText;
    }

    public void setmBtText(String mBtText) {
        this.mBtText = mBtText;
    }

    public int getmBtImage() {
        return mBtImage;
    }

    public void setmBtImage(int mBtImage) {
        this.mBtImage = mBtImage;
    }
}
